package es.urjc.jer;

public class Player {

	private String id;
	private String password;
	
	public Player(){}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	};
	
	
	
}
